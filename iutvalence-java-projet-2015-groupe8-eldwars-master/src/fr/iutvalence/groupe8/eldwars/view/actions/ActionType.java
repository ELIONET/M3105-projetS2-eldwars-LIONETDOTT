package fr.iutvalence.groupe8.eldwars.view.actions;

/**
 * This Enum is used to determine the types of actions.
 * 
 * @author Nicolas
 * @version 20150608
 */
public enum ActionType
{
	/**
	 * The move action.
	 */
	MOVE,

	/**
	 * The recruit action.
	 */
	RECRUIT,

	/**
	 * The attack action.
	 */
	ATTACK,

	/**
	 * The upgrade action.
	 */
	UPGRADE,

	/**
	 * The next round action.
	 */
	NEXT_ROUND;
}
